/**
 * Obtiene la imagen de una categorÃ­a especÃ­fica
 */
export const getCategoryImage = (categoryKey: string, type: 'ICON' | 'BG' = 'ICON') => {
  const category = CATEGORIES[categoryKey as keyof typeof CATEGORIES];
  return category ? category[type as keyof typeof category] : PLACEHOLDERS.CATEGORY_DEFAULT;
};

/**
 * Obtiene la imagen de mascota segÃºn el estado de Ã¡nimo
 */
export const getMascotaMood = (mood: 'happy' | 'sad' | 'thinking' | 'celebrating' | 'sleeping' | 'working' | 'exercising' | 'eating' | 'reading' = 'default') => {
  return MASCOTA[mood.toUpperCase() as keyof typeof MASCOTA] || MASCOTA.DEFAULT;
};

/**
 * Obtiene imagen decorativa aleatoria
 */
export const getRandomDecorative = () => {
  const keys = Object.keys(DECORATIVE);
  const randomKey = keys[Math.floor(Math.random() * keys.length)];
  return DECORATIVE[randomKey as keyof typeof DECORATIVE];
};
